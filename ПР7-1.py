print("Введите длины трех сторон треугольника:")
a = float(input("Сторона a: "))
b = float(input("Сторона b: "))
c = float(input("Сторона c: "))
p = (a + b + c) / 2
s = (p * (p - a) * (p - b) * (p - c)) ** 0.5

# Выводим результат
print(f"Полупериметр треугольника: {p}")
print(f"Площадь треугольника: {s}")